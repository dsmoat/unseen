const sig = (
  id: string,
  type: BusinessSignal["type"],
  title: string,
  sourceMessageIds: string[],
  tags: string[],
  confidence = 0.86,
): BusinessSignal => ({
  id,
  type,
  title,
  description: title,
  entityIds: tags,
  sourceMessageIds,
  firstObservedAt: messages.find((m) => m.id === sourceMessageIds[0])!.sentAt,
  lastObservedAt: messages.find((m) => m.id === sourceMessageIds.at(-1))!
    .sentAt,
  confidence,
  extractionMode: "deterministic",
  status: "unreviewed",
  tags,
});
